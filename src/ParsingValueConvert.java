public class ParsingValueConvert {

    public static int gettheparsingconvert(String x){
        int newconvertedvariable = Integer.parseInt(x);
        return newconvertedvariable;
    }
    public static String gettheparsingconvert(int x){
        String newconvertedvariable = Integer.toString(x);
        return newconvertedvariable;
    }
    public static Double gettheparsingconvertdouble(String x){
        Double newconvertedvariable = Double.parseDouble(x);
        return newconvertedvariable;
    }

}
